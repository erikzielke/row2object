package row2object.sample;

import javax.validation.constraints.Min;
import row2object.columns.ColumnName;

public class Person {
    private int id;
    @ColumnName("alder")
    @Min(18)
    private int age;
    @ColumnName("navn")
    private String name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Person{" +
                "id=" + id +
                ", age=" + age +
                ", name='" + name + '\'' +
                '}';
    }
}
